#include "Cube3d.h"

void	dda_calcul(t_game *game)
{
	while (!game->hit)
	{
		if (game->sidedistx < game->sidedisty)
		{
			game->sidedistx += game->deltadistx;
			game->mapx += game->stepx;
			game->side = 0;
		}
		else
		{
			game->sidedisty += game->deltadisty;
			game->mapy += game->stepy;
			game->side = 1;
		}
		if (game->smap[game->mapy][game->mapx] == '1')
			game->hit = 1;
	}
}
